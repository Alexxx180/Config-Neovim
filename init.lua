  local addr = './godot.pipe'
  if vim.fn.has 'win32' == 1 then
    -- Exec Path: nvim
    -- Exec Flags: --server 127.0.0.1:6004 --remote-send "<esc>:n {file}<CR>:call cursor({line},{col})<CR>"
    addr = '127.0.0.1:6004'
  end
  vim.fn.serverstart(addr)
end

